    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
        Usuario u;
        if (txtUsuario.getText().equals("")) {
            lblErro.setText("Erro. O campo de usuário está vazio.");
        } else if (txtSenha.getText().equals("")) {
            lblErro.setText("Erro. O campo de senha está vazio.");
        } else if (!b.verificarUsuario(txtUsuario.getText())) {
            lblErro.setText("");
            if (txtSenha.getText().equals(txtSenhaConf.getText())) {
                if (isPremium.isSelected()) {
                    u = new UsuarioPremium(txtUsuario.getText(), txtSenha.getText());
                } else {
                    u = new UsuarioComum(txtUsuario.getText(), txtSenha.getText());
                }

                try {
                    inscreverUsuario(u);
                    lblErro.setText("Usuário cadastrado com sucesso.");
                } catch (IOException ex) {
                    Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                lblErro.setText("Erro. Senhas não correspondentes.");
            }
        } else {
            lblErro.setText("Erro. Usuário já existe.");
        }
    }//GEN-LAST:event_btnCadastroActionPerformed

