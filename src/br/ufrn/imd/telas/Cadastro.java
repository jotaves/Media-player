    private void inscreverUsuario(Usuario n) throws IOException {
        FileWriter writer = new FileWriter(file, true);
        //BufferedWriter writer = new BufferedWriter(new FileWriter(file));
        writer.append(n.toString() + "\n");
        b.adicionar(n);
        writer.flush();
        writer.close();
    }
